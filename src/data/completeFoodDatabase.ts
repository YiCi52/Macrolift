// Funciones de búsqueda y filtrado
export function searchFoods(searchTerm: string): Food[] {
  if (!searchTerm.trim()) {
    return [...completeFoodDatabase, ...colombianFoods];
  }
  
  const term = searchTerm.toLowerCase().trim();
  return [...completeFoodDatabase, ...colombianFoods].filter(food => 
    food.name.toLowerCase().includes(term) ||
    food.category.toLowerCase().includes(term)
  );
}

export function getFoodsByCategory(category: string): Food[] {
  return [...completeFoodDatabase, ...colombianFoods].filter(food => food.category === category);
}

export function getAllCategories(): string[] {
  const categories = [...new Set([...completeFoodDatabase, ...colombianFoods].map(food => food.category))];
  return categories.sort();
}

export function getFoodById(id: string): Food | undefined {
  return [...completeFoodDatabase, ...colombianFoods].find(food => food.id === id);
}

export function databaseStats() {
  const allFoods = [...completeFoodDatabase, ...colombianFoods];
  return {
    totalFoods: allFoods.length,
    categories: getAllCategories().length,
    categoriesBreakdown: getAllCategories().map(category => ({
      name: category,
      count: getFoodsByCategory(category).length
    }))
  };
}

